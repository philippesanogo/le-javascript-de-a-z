/**
 * Les tableaux
 */


let tableauDeNombre = [1,2,3,4,7];
console.log(tableauDeNombre);

// mettre à jour un élément du tableau
tableauDeNombre[4] = 5;
console.log(tableauDeNombre);

// ajouter un élément à la fin du tableau
tableauDeNombre.push(6);
console.log(tableauDeNombre);

// retirer un élément à la fin du tableau
tableauDeNombre.pop();
console.log(tableauDeNombre);

// ajouter un élément au début du tableau
tableauDeNombre.unshift(0);
console.log(tableauDeNombre);

// retirer un élément au début du tableau
tableauDeNombre.shift();
console.log(tableauDeNombre);

// afficher la longueur du tableau
tableauDeNombre.length;
console.log(tableauDeNombre.length);
